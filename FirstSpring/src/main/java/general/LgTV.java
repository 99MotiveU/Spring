package general;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTV - 전원을 켠다.");
	}
	public void turnOFF() {
		System.out.println("LgTV - 전원을 끈다.");
	}
	public void soundUp() {
		System.out.println("LgTV - 볼륨을 높인다.");
	}
	public void soundDown() {
		System.out.println("LgTv - 볼륨을 낮춘다.");
	}
}
